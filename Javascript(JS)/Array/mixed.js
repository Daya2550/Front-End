// Declaring a mixed array
let mixedArray=[1, "Hello", true, null, undefined, {name: "Alice"}, [1, 2, 3]];
console.log(mixedArray); // Output: [1, "Hello", true, null, undefined, {name: "Alice"}, [1, 2, 3]]



// Accessing elements of the mixed array
console.log(mixedArray[0]); // Output: 1
console.log(mixedArray[1]); // Output: "Hello"
console.log(mixedArray[2]); // Output: true


